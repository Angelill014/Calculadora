using System;

public class Calculadora
{
     static int numero1=0;
     static int numero2=0;
    public static int Suma()
    {
      return numero1+numero2;
     
    }
    public static int Resta()
    {
      return numero1-numero2;
    }
    public static int Multiplicacion()
    {
      return numero1*numero2;
    }
    public static int Division()
    {
      return numero1/numero2;
    }
  
}

